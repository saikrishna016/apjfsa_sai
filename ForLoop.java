package practise;

public class ForLoop {
	public static void main(String[] args) {
	
int num =9,i;

for (i=1;i<=10;i++) {
	System.out.println(num+ "x" +i+"="+(num*i));
}
     
for(int j=22;j>11;--j) {
	System.out.println(num+ "x" +j+"="+(num*j));
}
     for(int k=3; k<11;k++) {
    	 System.out.println(+k);
     }
    	
}}
 